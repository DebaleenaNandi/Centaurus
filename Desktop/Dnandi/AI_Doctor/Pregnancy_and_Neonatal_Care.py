def generate_speech(text, language_code, filename):
    tts = gTTS(text=text, lang=language_code, slow=False)
    tts.save(filename)
    os.system(f"start {filename}")  # This will play the speech on Windows. For macOS use `afplay` and Linux `mpg321`

def select_language():
    print("Please select a language from the options below:")
    for i, language in enumerate(language_codes.keys(), 1):
        print(f"{i}. {language}")

    choice = int(input("Enter the number corresponding to your choice: "))
    languages = list(language_codes.keys())
    
    if 1 <= choice <= len(languages):
        selected_language = languages[choice - 1]
        print(f"You selected {selected_language}. Generating speech...")
        return selected_language
    else:
        print("Invalid choice. Please select a valid option.")
        return select_language()

